/**
 * 法務文書（プライバシーポリシー・利用規約）の本文。
 *
 * - app/legal/privacy-policy.tsx と app/legal/terms-of-use.tsx から参照
 * - docs/legal/*.md に同じ内容を Markdown で並行管理（GitHub Pages 等で外部公開する際の元データ）
 * - 文面を変更したら docs/legal/ 側も忘れず更新すること
 *
 * 設計原則（REQUIREMENTS.md §7.2）の文章化:
 * - 画像はサーバー・ログに保存しない（Worker 即破棄）
 * - 位置情報は端末内処理、サーバー送信なし
 * - 端末ID は SHA-256 ハッシュ化、レート制限のみに使用
 * - 第三者提供なし
 * - アクセス解析未導入
 */

export interface LegalSection {
  /** 「1. はじめに」の見出し本文（番号は LegalDocumentScreen 側で自動付与） */
  heading: string;
  /** 段落本文。各要素が 1 つの段落になる */
  body: string[];
  /** 必要なら箇条書き（body の後に出力される） */
  bullets?: string[];
}

export interface LegalRevision {
  date: string;
  note: string;
}

export interface LegalDocument {
  title: string;
  effectiveDate: string;
  contactEmail: string;
  /** 冒頭の挨拶／要約。SectionBlock より上に出る */
  intro: string;
  sections: LegalSection[];
  revisionHistory: LegalRevision[];
}

